// trace function and TRACE macro

#ifdef _DEBUG
#include <stdio.h>

void Trace(const wchar_t* pszFormat, ...)
{
	wchar_t szOutput[1024];
	va_list	vaList;

	va_start(vaList, pszFormat);
	vswprintf(szOutput, _countof(szOutput), pszFormat, vaList);
	::OutputDebugString(szOutput);
}

#endif // _DEBUG

